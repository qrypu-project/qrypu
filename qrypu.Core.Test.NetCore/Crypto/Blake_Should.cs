        private void Pass_BatteryTest(string[] hexTestBatery, string[] hexExpHash, int bitLen)
        {
            var blake = BlakeHashAlgorithm.Create(bitLen);

            for (int t = 0; t < hexTestBatery.Length; t++)
            {
                var data = TestUtils.FromHex(hexTestBatery[t]);

                using (var stream = new MemoryStream(data))
                {
                    var hash = blake.ComputeHash(stream);
                    var hashText = String.Join("", hash.Select(b => b.ToString("X2")));

                    Debug.WriteLine("hash {0}: {1}", t, hashText);
                    Assert.AreEqual(hexExpHash[t], hashText);
                }
            }
        }
